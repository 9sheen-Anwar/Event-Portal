            <h3><i>
            
            <ul>
            <li>Mark out the play area with chalk or string. Then draw a line down the middle, cutting the field in half length-ways. Put a pole, stick or sweater at each end of the central dividing line.</li>
            <li>Split the Kho Kho players into two equal teams, and then allocate one team as the chasers and one team as the defenders (let them know they'll get to swap roles after seven minutes).</li>
            <li>Chasers need to line up on the line down the middle, facing in alternate directions. They can only 'chase' on the side of the pitch they are facing and can only chase one at a time </li>
            <li>The defenders enter the field in groups of three and need to avoid being tagged by a chaser - they can run anywhere on the field, but they're out if they get tagged.</li>
            <li>The chaser at the pole starts and must try to tag one of the defenders on their side of the pitch, if a defender crosses the line to the other side, the chaser must tap the back of one of his teammates, who is sitting facing the other direction, and shout "Kho!"</li>
            <li>The teammate must then try to tag the defender and the standing chaser sits in the team mates place so only one chaser is chasing.</li>
            <li>Chasers can swap with a teammate every time the defender moves into the opposite side of the pitch or the chaser can run round one of the poles to get to the other side of the pitch.</li>
            <li>The aim for chasers is to tag-out the defenders the fastest. Whichever team gets the defenders out the quickest wins.</li>
            </ul>
            
            
