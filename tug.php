            <dt style="margin-left: 100px; margin-right: 100px;"><h1 style="color: maroon;">Rules</h1></dt>
            <dd style="margin-left: 100px; margin-right: 100px;">
            <h3><i>
            
            <ul>
            <li>Each team in a Tug of War competition consists of eight people.</li>
            <li>There are various weight classifications in Tug of War, and the mass of the eight people combined must not weigh more than that determined by the category that they are placed in.</li>
            <li>The rope used should be of a circumference of approximately 11 cm and should be marked in the middle with a centre line as well as two marks that should be placed 4m from the centre line.</li>
            <li>At the start of the pull, the centre line of the rope should be immediately above line marked on the ground.</li>
            <li>Both teams pull the rope, the winner being the team who manage to pull the mark on the rope closest to their opponents over the centre line.</li>
            <li>The rope must be pulled underarm and nobody's elbow must go below the knee, otherwise a foul will be called.</li>
            <li>Matches are often a best of three pull, the winner winning two out of the three pulls.</li>
            
            </ul>
            
            
            </i></h3>
