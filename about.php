For the empowerment of women, especially from the Muslim community, Women's Polytechnic (Faculty of Engg. & Technology) was established in the year 
1966 as an adjunct institute to the University Polytechnic (Boys) to impart technical education to the women candidates. Mr. S.A. Abbas was the 
founder principal of this institute. In the beginning the polytechnic started with three diploma courses:
<br /><br />
(i) Diploma in Engineering (Electronics/TV Technology)<br /><br />

(ii) Diploma in Office Assistance and Secretarial Practice<br /><br />

(iii) Diploma in Costume Design and Dress Making.<br /><br />

In February 1988, University Women's Polytechnic became a separate full fledged Polytechnic with three Diploma courses as mentioned above.
 Diploma in Computer Engineering Course was started from the session 1995-96 under a newly created Computer Engineering Section. The Computer 
 Engineering Section started a new course in Diploma in Information Technology from the session 2002-2003 replacing Diploma in TV Technology.
  Costume Design and Dress Making section is offering a new course in Interior Design named Advanced Diploma in Interior Decoration from the
   session 2012 - 2013. At present, the following Diploma Courses are being offered by this Institute.
